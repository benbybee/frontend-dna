// ── Helpers ─────────────────────────────────────────────────────────────────

function isColorDark(color: string): boolean {
  const hex = color.replace("#", "");
  if (hex.length !== 6) return false;
  const r = parseInt(hex.substring(0, 2), 16);
  const g = parseInt(hex.substring(2, 4), 16);
  const b = parseInt(hex.substring(4, 6), 16);
  const luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  return luminance < 0.5;
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function getTypographyRole(element: string, fontSize: string): string {
  const size = parseFloat(fontSize);
  if (element === "h1" || size >= 40) return "Display Hero";
  if (element === "h2" || size >= 28) return "Section Heading";
  if (element === "h3" || size >= 22) return "Sub-heading";
  if (element === "h4" || size >= 18) return "Body Large";
  if (element === "h5") return "Label";
  if (element === "h6") return "Caption";
  if (element === "button") return "Button";
  if (element === "input") return "Input";
  if (element === "a") return "Link";
  if (element === "li") return "List Item";
  if (element === "label") return "Label";
  if (element === "figcaption") return "Caption";
  if (size >= 18) return "Body Large";
  if (size >= 14) return "Body";
  if (size >= 12) return "Caption";
  return "Micro";
}

function detectBaseUnit(scale: number[]): number {
  if (scale.length < 2) return 4;
  const diffs = scale.slice(1).map((v, i) => v - scale[i]);
  const gcd = diffs.reduce((a, b) => {
    while (b) { [a, b] = [b, a % b]; }
    return Math.abs(a);
  });
  return gcd > 0 && gcd <= 8 ? gcd : 4;
}

function classifyShadows(shadows: string[]): ElevationLevel[] {
  if (shadows.length === 0) return [];

  // Sort by estimated "intensity" (blur radius)
  const parsed = shadows.map((s) => {
    const blurMatch = s.match(/(\d+)px\s+(\d+)px\s+(\d+)px/);
    const blur = blurMatch ? parseInt(blurMatch[3]) : 0;
    return { shadow: s, blur };
  }).sort((a, b) => a.blur - b.blur);

  const levels: ElevationLevel[] = [];
  const names = ["Subtle", "Standard", "Elevated", "Deep", "Dramatic"];
  const usages = [
    "Hover hints, subtle lift",
    "Cards, content panels",
    "Featured cards, dropdowns",
    "Modals, floating panels",
    "Maximum emphasis, overlays",
  ];

  for (let i = 0; i < Math.min(parsed.length, 5); i++) {
    levels.push({
      name: names[i] || `Level ${i + 1}`,
      level: i + 1,
      shadow: parsed[i].shadow,
      usage: usages[i] || "Custom elevation",
    });
  }

  return levels;
}
